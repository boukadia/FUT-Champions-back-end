function formuleShow(event) {
  formulaire=event.target.parentElement;
  let targetPosition = event.target.parentElement.querySelector(".position").textContent;
  remplacant.style.display = "flex";
  playerCards.forEach((card) => {
    let cardPosition = card.querySelector(".posi").textContent;
    console.log(card)
   
    if (cardPosition == targetPosition) {
      card.style.display = "flex-wrap"; 
      
    } else {
      card.style.display = "none"; 
    }
  });
}
